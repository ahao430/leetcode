/**
 * Definition for a binary tree node.
 * function TreeNode(val) {
 *     this.val = val;
 *     this.left = this.right = null;
 * }
 */
/**
 * @param {TreeNode} root
 * @return {number[][]}
 */
var levelOrderBottom = function(root) {
  let res = []
  order(root, 0)
  function order (node, l) {
    if (node != null) {
      if (res[l]) {
        res[l].push(node.val)
      } else {
        res[l] = [node.val]
      }
      order(node.left, l + 1)
      order(node.right, l + 1)
    }
  }
  return res.reverse()
};

